package ma.ensaj.StaySafe.entity;

public enum LocationStatus {
    ACTIVE, RESOLVED, EXPIRED
}
/*

ACTIVE : Indique que la localisation est actuellement active ou en cours d'utilisation.
RESOLVED : Indique que la localisation a été traitée ou résolue (par exemple, une situation d'urgence a été prise en charge).
EXPIRED : Indique que la localisation n'est plus valide ou qu'elle est arrivée à échéance (par exemple, une ancienne localisation dépassée).
 */